    private void total() {
        jTableTotal.setModel(mr.mostrar(jTextFieldFecha.getText()));
        suma();
        JOptionPane.showMessageDialog(this, "Se vendio "+Double.toString((int) suma())+ " el d√≠a " + jTextFieldFecha.getText());
    }
    

