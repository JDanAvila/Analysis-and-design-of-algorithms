    public double suma() {
        double contar = jTableTotal.getRowCount();
        double suma = 0;
        for (double i = 0; i < contar; i++) {
            suma = suma + Double.parseDouble(jTableTotal.getValueAt((int) i, 1).toString());
        }
        //JOptionPane.showMessageDialog(this, suma);
        return suma;
    }
 
